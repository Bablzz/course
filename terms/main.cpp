int main() {
  // Вводим число N
  int N;
  i = j = 0;
  cout << "N = ";
  cin >> N;
  L = N;
  for (i = 1; i <= L; i++) {
    X[i] = 1;
  };
  i = 0;
  for (i = 1; i <= L; i++) {
    cout << X[i] << " " << endl;
    do {
       sum(X, L);
       for (j = 1; j<=L; j++){
        cout << X[j] << " " << endl;
       };
    } while (L=1);
  }
  // Вывод разложения на слагаемые

  return 0;
}
